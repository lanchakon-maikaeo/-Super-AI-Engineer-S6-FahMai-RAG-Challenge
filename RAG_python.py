def wait_for_rate_limit():
    global request_timestamps
    now = time.time()
    while request_timestamps and now - request_timestamps[0] >= 60: request_timestamps.popleft()
    if len(request_timestamps) >= 200:
        sleep_time = 60 - (now - request_timestamps[0])
        if sleep_time > 0: time.sleep(sleep_time)
        now = time.time()
    if request_timestamps and now - request_timestamps[-1] < 0.2:
        sleep_time = 0.2 - (now - request_timestamps[-1])
        if sleep_time > 0: time.sleep(sleep_time)
        now = time.time()
    request_timestamps.append(now)

def ask_llm(messages, model, max_tokens=2024, max_retries=5):

    url = f"http://thaillm.or.th/api/{model}/v1/chat/completions"
    headers = {"Content-Type": "application/json", "apikey": THAILLM_API_KEY}
    payload = {"model": "/model", "messages": messages, "max_tokens": max_tokens, "temperature": 0.0}

    for attempt in range(max_retries):
        try:
            resp = requests.post(url, headers=headers, json=payload, timeout=120)

            # Rate limit — wait and retry
            if resp.status_code == 429:
                wait = min(2 ** attempt, 30)
                print(f"  Rate limited, waiting {wait}s...")
                time.sleep(wait)
                continue

            resp.raise_for_status()
            return resp.json()["choices"][0]["message"]["content"].strip()

        except requests.exceptions.RequestException as e:
            wait = 2 ** attempt
            print(f"  Error: {e}, retrying in {wait}s...")
            time.sleep(wait)

    return None

def generate_expanded_queries(query):
    sys_prompt = "คุณคือ AI ผู้ช่วยค้นหาข้อมูล หน้าที่ของคุณคือสร้าง 'คำค้นหา (Search Keyword)' เพิ่มเติม 2 ประโยคจากคำถามที่ให้มา เพื่อนำไปใช้ค้นหาสเปคสินค้าใน Database ตอบกลับแค่คำค้นหา 2 ประโยคคั่นด้วยเครื่องหมายจุลภาค (,) ห้ามมีคำอธิบายใดๆ"
    messages = [{"role": "system", "content": sys_prompt}, {"role": "user", "content": query}]
    ans = ask_llm(messages, model="typhoon", max_tokens=100, max_retries=3) # ใช้ typhoon เป็นตัวสร้าง Query
    if ans: return [q.strip() for q in ans.split(",") if q.strip()]
    return []

def parse_cot_answer(text):
    if text is None: return 9, False
    clean_text = re.sub(r"<think>.*?</think>", "", text, flags=re.DOTALL).strip()
    match = re.search(r'Answer:\s*([1-9]|10)', clean_text, re.IGNORECASE)
    if match: return int(match.group(1)), True
    numbers = re.findall(r'\b(?:10|[1-9])\b', clean_text)
    if numbers: return int(numbers[-1]), True
    return 9, False

