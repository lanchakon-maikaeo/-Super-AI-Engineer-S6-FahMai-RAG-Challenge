def hybrid_retrieve(original_query, expanded_queries=[], fetch_k=FETCH_K):
    all_queries = [original_query] + expanded_queries
    bm25_idx = set()
    dense_idx = set()

    global chunk_embeddings
    if not torch.is_tensor(chunk_embeddings):
        chunk_embeddings = torch.tensor(chunk_embeddings)

    for q in all_queries:
        # Sparse (BM25)
        tokens = word_tokenize(q, engine="newmm")
        bm25_idx.update(np.argsort(bm25.get_scores(tokens))[::-1][:fetch_k])

        # Dense (mE5)
        q_emb = embed_model.encode(f"query: {q}", normalize_embeddings=True, convert_to_tensor=True).to(chunk_embeddings.device)
        chunk_embeddings = chunk_embeddings.to(q_emb.device)
        dense_scores = torch.matmul(q_emb, chunk_embeddings.T).squeeze(0)
        dense_idx.update(torch.topk(dense_scores, k=fetch_k).indices.cpu().numpy())

    candidate_indices = list(bm25_idx.union(dense_idx))
    candidate_texts = [chunk_texts[i] for i in candidate_indices]

    # Rerank ด้วยคำถามต้นฉบับเท่านั้น
    cross_inp = [[original_query, text] for text in candidate_texts]
    rerank_scores = reranker.predict(cross_inp)
    sorted_idx = np.argsort(rerank_scores)[::-1]

    # --- 🌟 Dynamic TOP_K (เลือกเฉพาะข้อมูลที่มีคะแนนเกาะกลุ่มกับที่ 1) ---
    best_score = rerank_scores[sorted_idx[0]]
    best_texts = []

    for idx in sorted_idx[:5]: # ดึงมาสูงสุดไม่เกิน 5 ก้อน
        # หากคะแนนห่างจากที่ 1 ไม่เกิน 2.5 logit ถือว่าเป็นข้อมูลที่เกี่ยวข้อง
        if rerank_scores[idx] >= best_score - 2.5:
            best_texts.append(candidate_texts[idx])

    return "\n\n---\n\n".join(best_texts)

