def unroll_markdown_table(text):
    lines = text.split('\n')
    unrolled, in_table, headers = [], False, []

    for line in lines:
        if line.strip().startswith('|') and line.strip().endswith('|'):
            cells = [c.strip() for c in line.split('|')[1:-1]]
            if not in_table:
                headers = cells
                in_table = True
            elif set(list(line.strip())) <= set(['|', '-', ' ', ':']):
                continue
            else:
                row_data = [f"{headers[i]}: {cell}" for i, cell in enumerate(cells) if i < len(headers) and cell and cell != "-"]
                unrolled.append("📌 ข้อมูลสเปค -> " + " | ".join(row_data))
        else:
            in_table = False
            unrolled.append(line)
    return '\n'.join(unrolled)

def clean_text_normalization(text):
    text = re.sub(r'\[([^\]]+)\]\([^\)]+\)', r'\1', text)
    return unroll_markdown_table(text)

md_files = glob.glob(f"{DATA_DIR}/**/*.md", recursive=True)

